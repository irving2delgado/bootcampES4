// Function to display even numbers in the range 1 to 100
function displayEvenNumbers() {
    // Loop through the range from 1 to 100
    for (let i = 1; i <= 100; i++) {
        // Check if the current number is even
        if (i % 2 === 0) {
            // Display the even number on the screen
            console.log(i);
        }
    }
}

// Call the function to display even numbers
displayEvenNumbers();
